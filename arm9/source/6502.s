irq6502:
@---------------------------------------------------------------------------------
	ldr r12,=IRQ_VECTOR
	bl Vec6502
	fetch 7
@---------------------------------------------------------------------------------
Vec6502:
@---------------------------------------------------------------------------------
	ldr_ r0,lastbank
	sub r0,m6502_pc,r0
	push16					@save PC

	encodeP (R)				@save P
VecCont:
	push8 r0

	orr cycles,cycles,#CYC_I	@disable IRQ
@	bic cycles,cycles,#CYC_D	@and decimal mode

	ldr_ r0,memmap_tbl+7*4
	ldrb m6502_pc,[r0,r12]!
	ldrb r2,[r0,#1]
	orr m6502_pc,m6502_pc,r2,lsl#8
	encodePC				@get IRQ vector

	bx lr
