hk0:
default_scanlinehook:
@---------------------------------------------------------------------------------
	fetch 0
@---------------------------------------------------------------------------------
CheckI:								@Check Interrupt Disable
@---------------------------------------------------------------------------------
	tst cycles,#CYC_I
	bne default_scanlinehook		@we dont want no stinkin irqs
