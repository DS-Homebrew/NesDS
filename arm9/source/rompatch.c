unsigned int romcrc(unsigned char * c, int size)
{
	unsigned int r = 0xFFFFFFFFUL;
	while( --size >= 0 ) {
		r = (r >> 8) ^ crctable[(unsigned char)r ^ *c++];
	}
	return r ^ 0xFFFFFFFFUL;
}

void crcinit()
{
	int	i, j;
	unsigned int	r;

	for( i = 0; i <= 255; i++ ) {
		r = i;
		for( j = 0; j < 8; j++ ) {
			if( r & 1 ) r = (r >> 1) ^ 0xEDB88320UL;
			else        r >>= 1;
		}
		crctable[i] = r;
	}
}

