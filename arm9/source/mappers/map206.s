;@----------------------------------------------------------------------------
	#include "mmc3.i"
;@----------------------------------------------------------------------------
	.global mapper88init
	.global mapper95init
	.global mapper154init
	.global mapper206init

;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
;@ Tengen's "MIMIC-1" and Namcot's "108", "109", "118", and "119".
;@ See also mapper 76
mapper206init:
;@----------------------------------------------------------------------------
	.word mappingW, rom_W, rom_W, rom_W
	b mmc3Init
;@----------------------------------------------------------------------------
;@ Namcot 108, 118
;@ Dragon Spirit - Aratanaru Densetsu
;@ Namcot Mahjong 3 - Mahjong Tengoku
;@ Quinty (J)
mapper88init:
;@----------------------------------------------------------------------------
	.word mapping88W,rom_W,rom_W,rom_W
	b mmc3Init
;@----------------------------------------------------------------------------
;@ Namcot 108
;@ Dragon Buster (J)
mapper95init:
;@----------------------------------------------------------------------------
	.word mapping95W,rom_W,rom_W,rom_W
	b mmc3Init
;@----------------------------------------------------------------------------
;@ Namcot 108
;@ Devil Man (J)
mapper154init:
;@----------------------------------------------------------------------------
	.word mapping88W,write1,write1,write1
	b mmc3Init

;@----------------------------------------------------------------------------
mappingW:		;@ 8000-9FFF
;@----------------------------------------------------------------------------
	tst addy,#1
	andne r0,r0,#0x3F
	andeq r0,r0,#0x07
	b mmc3MappingW
;@----------------------------------------------------------------------------
mapping95W:		;@ 8000-9FFF
;@----------------------------------------------------------------------------
	tst addy,#1
	andeq r0,r0,#0x07
	beq mmc3Mapping0W
	andne r0,r0,#0x3F
	stmfd sp!,{r0,lr}
	tst r0,#0x20
	bl mirror1_
	ldmfd sp!,{r0,lr}
	b mmc3Mapping1W
;@----------------------------------------------------------------------------
mapping88W:		;@ 8000-9FFF
;@----------------------------------------------------------------------------
	tst addy,#1
	andeq r0,r0,#0x07
	beq mmc3Mapping0W

	ldrb_ r1,reg0
	and r0,r0,#0x3F
	cmp r1,#2
	orrcs r0,r0,#0x40
	b mmc3Mapping1W
;@----------------------------------------------------------------------------
write1:		;@ $8000-$FFFF
;@----------------------------------------------------------------------------
	tst r0,#0x40
	b mirror1_
;@----------------------------------------------------------------------------
