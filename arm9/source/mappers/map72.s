@---------------------------------------------------------------------------------
	#include "equates.h"
@---------------------------------------------------------------------------------
	.global mapper72init
@---------------------------------------------------------------------------------
.section .text,"ax"
@---------------------------------------------------------------------------------
@ Jaleco JF-17 board.
@ Used in:
@ Moero!! Juudou Warroirs...
@ Moero!! Pro Tennis
@ Pinball Quest
@ Also see mapper 92
@---------------------------------------------------------------------------------
mapper72init:
@---------------------------------------------------------------------------------
	.word write72,write72,write72,write72

	bx lr
@---------------------------------------------------------------------------------
write72:
@---------------------------------------------------------------------------------
	stmfd sp!,{r0,lr}
	tst r0,#0x80
	blne map89AB_
	ldmfd sp!,{r0,lr}
	tst r0,#0x40
	bne chr01234567_
	bx lr
@---------------------------------------------------------------------------------
