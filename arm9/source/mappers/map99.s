;@----------------------------------------------------------------------------
	#include "equates.h"
;@----------------------------------------------------------------------------
	.global mapper99init
;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
;@ Vs. System
;@ Used in:
;@ Vs. Super Mario Bros
;@ Vs. Ice Climber
mapper99init:
;@----------------------------------------------------------------------------
	.word rom_W,rom_W,rom_W,rom_W

	ldrb_ r0,cartFlags
	orr r0,r0,#VS
	strb r0,[r1]

	adr r0,write4016
	str_ r0,rp2A03IOWrite
	ldr r0,=vsJoy0_R		;@ $4016: controller 1
	str_ r0,rp2A03IORead0
	ldr r0,=vsJoy1_R		;@ $4017: controller 2
	str_ r0,rp2A03IORead1
	adr r0,countCoins
	str_ r0,rp2A03MemWrite

	bx lr
;@----------------------------------------------------------------------------
write4016:
;@----------------------------------------------------------------------------
	stmfd sp!,{r0,lr}

	mov r0,r0,lsr#2
	bl chr01234567_

	ldmfd sp!,{r0,lr}
	b standardJoy_W
;@----------------------------------------------------------------------------
countCoins:
;@----------------------------------------------------------------------------
	bx lr
;@----------------------------------------------------------------------------
