	.global mapper76init

	.struct mapperData
cmd: .byte
;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
;@ Namcot 108 on NAMCOT-3446 board
;@ Used in:
;@ Megami Tensei: Digital Devil Story
;@ Also see mapper 88, 206
mapper76init:
;@----------------------------------------------------------------------------
	.word write0,rom_W,rom_W,rom_W

	bx lr
;@----------------------------------------------------------------------------
write0:		;@ $8000-8001
;@----------------------------------------------------------------------------
	tst addy,#1
	andeq r0,r0,#0x07
	streqb_ r0,cmd
	bxeq lr
w8001:
	and r0,r0,#0x3F
	ldrb_ r1,cmd
	ldr pc,[pc,r1,lsl#2]
	nop
commandList:	.word void,void,chr01_,chr23_,chr45_,chr67_,map89_,mapAB_
;@----------------------------------------------------------------------------
