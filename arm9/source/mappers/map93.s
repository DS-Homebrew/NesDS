;@----------------------------------------------------------------------------
	#include "equates.h"
;@----------------------------------------------------------------------------
	.global mapper93init
;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
;@ Sunsoft-2 IC, Sunsoft-3R board
;@ Used in:
;@ Fantazy Zone (J)
;@ Shanghai
;@ Also see mapper 89, 184
mapper93init:
;@----------------------------------------------------------------------------
	.word write93,write93,write93,write93

	bx lr
;@----------------------------------------------------------------------------
write93:
;@----------------------------------------------------------------------------
	stmfd sp!,{r0,lr}
	tst r0,#1
	bl mirror2V_
	ldmfd sp!,{r0,lr}
	mov r0,r0,lsr#4
	b map89AB_
;@----------------------------------------------------------------------------
