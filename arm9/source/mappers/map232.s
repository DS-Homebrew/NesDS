@---------------------------------------------------------------------------------
	#include "equates.h"
@---------------------------------------------------------------------------------
	.global mapper232init
	mapbyte1 = mapperData+0
	mapbyte2 = mapperData+1
@---------------------------------------------------------------------------------
.section .text,"ax"
@---------------------------------------------------------------------------------
@ Camerica/Codemasters mapper
@ Used in:
@ Quattro Adventure
@ Quattro Sports
@ Quattro Arcade
@ Also see mapper 71
mapper232init:
@---------------------------------------------------------------------------------
	.word write0,write0,write1,write1

	mov r0,#0x18
	strb_ r0,mapbyte1

	bx lr
@---------------------------------------------------------------------------------
write0:
@---------------------------------------------------------------------------------
	and r0,r0,#0x18
	strb_ r0,mapbyte1
	b prgmap
@---------------------------------------------------------------------------------
write1:
@---------------------------------------------------------------------------------
	and r0,r0,#0x03
	strb_ r0,mapbyte2
	ldrb_ r0,mapbyte1
prgmap:
	mov addy,lr
	mov r1,#3
	orr r0,r1,r0,lsr#1
	bl mapCDEF_

	ldrb_ r0,mapbyte1
	ldrb_ r1,mapbyte2
	orr r0,r1,r0,lsr#1
	mov lr,addy
	b map89AB_
@---------------------------------------------------------------------------------
