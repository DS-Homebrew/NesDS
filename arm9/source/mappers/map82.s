@---------------------------------------------------------------------------------
	#include "equates.h"
@---------------------------------------------------------------------------------
	.global mapper82init
	cmd = mapperData
@---------------------------------------------------------------------------------
.section .text,"ax"
@---------------------------------------------------------------------------------
@ Taito X1-017 mapper IC
@ See also mapper 80
@---------------------------------------------------------------------------------
mapper82init:
@---------------------------------------------------------------------------------
	.word void,void,void,void

	adr r1,write0
	str_ r1,m6502WriteTbl+12
	bx lr
@---------------------------------------------------------------------------------
write0:		@$6000-7FFF
@---------------------------------------------------------------------------------
	tst addy,#1
	bne w8001

	strb_ r0,cmd
	bx lr
w8001:
	ldrb_ r1,cmd
	and r1,r1,#7
	ldr pc,[pc,r1,lsl#2]
	nop
commandlist:	.word void,void,chr01_,chr23_,chr45_,chr67_,map89_,mapAB_
@---------------------------------------------------------------------------------
write1:		@$A000
@---------------------------------------------------------------------------------
	tst addy,#1
	bxne lr
	tst r0,#1
	b mirror2V_
@---------------------------------------------------------------------------------
