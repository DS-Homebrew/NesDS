;@----------------------------------------------------------------------------
	#include "equates.h"
;@----------------------------------------------------------------------------
	.global mapper79init
	.global mapper148init
;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
;@ American Video Entertainment NINA-03 & NINA-06
;@ Also see mapper 113
mapper79init:
;@----------------------------------------------------------------------------
	.word chr01234567_,chr01234567_,chr01234567_,chr01234567_

	adr r1,write0
	str_ r1,rp2A03MemWrite

	mov r0,#0xff
	b map89ABCDEF_
;@----------------------------------------------------------------------------
;@ Sachen SA-008-A and Tengen 800008.
mapper148init:
;@----------------------------------------------------------------------------
	.word write148,write148,write148,write148
	mov r0,#0xff
	b map89ABCDEF_
;@----------------------------------------------------------------------------
write0:
;@----------------------------------------------------------------------------
	and r1,addy,#0xE100
	cmp r1,#0x4100
	bne empty_W
write148:
	stmfd sp!,{r0,lr}
	mov r0,r0,lsr#3
	bl map89ABCDEF_
	ldmfd sp!,{r0,lr}
	b chr01234567_
;@----------------------------------------------------------------------------
