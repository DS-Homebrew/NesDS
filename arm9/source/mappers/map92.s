@---------------------------------------------------------------------------------
	#include "equates.h"
@---------------------------------------------------------------------------------
	.global mapper92init

	reg0 = mapperData
@---------------------------------------------------------------------------------
.section .text,"ax"
@---------------------------------------------------------------------------------
@ Jaleco JF-17 board.
@ Used in:
@ Moero!! Pro Soccer
@ Moero!! Pro Yakyuu '88 Kettei Ban...
@ Also see mapper 72
mapper92init:
@---------------------------------------------------------------------------------
	.word write92,write92,write92,write92
	mov r0,#0
	b map89AB_
@---------------------------------------------------------------------------------
write92:
@---------------------------------------------------------------------------------
	ldrb_ r1,reg0
	strb_ r0,reg0
	eor r1,r1,r0
	and r1,r1,r0
	stmfd sp!,{r0,r1,lr}
//	ands r2,r0,#0x30		@ Sound bits, 0x20 is reset, 0x10 is select sound with adress bits.
	tst r1,#0x80
	blne mapCDEF_
	ldmfd sp!,{r0,r1,lr}
	tst r1,#0x40
	bne chr01234567_
	bx lr
@---------------------------------------------------------------------------------
