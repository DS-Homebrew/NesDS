;@----------------------------------------------------------------------------
	#include "mmc1.i"
;@----------------------------------------------------------------------------
	.global mapper1init
	.global mapper155init

;@----------------------------------------------------------------------------
.section .text,"ax"
;@----------------------------------------------------------------------------
mapper1init:
;@----------------------------------------------------------------------------
	.word mmc1Write0,mmc1Write1,mmc1Write2,mmc1Write3
	b mmc1Init
;@----------------------------------------------------------------------------
mapper155init:
;@----------------------------------------------------------------------------
	.word mmc1Write0,mmc1Write1,mmc1Write2,mmc1Write3
	stmfd sp!,{lr}
	bl mmc1Init
	ldr r0,=mmc1ACpuSwitch
	str_ r0,cpuSwitch
	ldmfd sp!, {pc}
