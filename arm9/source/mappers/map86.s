@---------------------------------------------------------------------------------
	#include "equates.h"
@---------------------------------------------------------------------------------
	.global mapper86init
@---------------------------------------------------------------------------------
.section .text,"ax"
@---------------------------------------------------------------------------------
@ Jaleco JF-13 board
@ Used in:
@ (the Red and Black releases of) Moero!! Pro Yakyuu, the Japanese version of Jaleco's Bases Loaded.
@ Urusei Yatsura...
mapper86init:
@---------------------------------------------------------------------------------
	.word void,void,void,write86
	adr r1,write86
	str_ r1,m6502WriteTbl+12
	mov r0,#0
	b map89ABCDEF_
@---------------------------------------------------------------------------------
write86:				@ 6000-7FFF (& E000-FFFF)
@---------------------------------------------------------------------------------
	tst addy,#0x1000
	bxne lr						@ 7000-7FFF Handle sound
	stmfd sp!,{r0,lr}
	mov r0,r0,lsr#4
	bl map89ABCDEF_
	ldmfd sp!,{r0,lr}
	and r1,r0,#0x40
	and r0,r0,#0x03
	orr r0,r0,r1,lsr#4
	b chr01234567_
@---------------------------------------------------------------------------------
